package labexercises;

public class Vowel {
	public static void main(String[] args) {
		
		char ch='i';

		if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
			System.out.println("The letter is a vowel");
			
		}
		
		else {
			System.out.println("The letter is a consonant");
		}
		
	}

}
